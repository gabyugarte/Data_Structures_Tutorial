'''Node class defines the structure of a tree node with a value and references to left and right children.
BinaryTree class initializes the root node of the tree and contains an insert method to add new nodes to the tree based on their values.'''

class Node:
    def __init__(self, key):
        self.left = None
        self.right = None
        self.val = key

class BinaryTree:
    def __init__(self):
        self.root = None

    def insert(self, root, key):
        if root is None:
            return Node(key)
        else:
            if root.val < key:
                root.right = self.insert(root.right, key)
            else:
                root.left = self.insert(root.left, key)
        return root
